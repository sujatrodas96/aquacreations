<p style="font-size: 18px; color: rgb(100,150,220);">The Bicolor Angelfish, also known as a Two-colored Angelfish or Oriole Angelfish, is a hardy fish with yellow on the first half of its body and blue on the second half.A 70 gallon tank with plenty of hiding places and live rock for grazing is best for this Angelfish. However, the Bicolor Angelfish is not a good reef dweller because it is prone to nip at stony and soft corals and clam mantles.</p>
