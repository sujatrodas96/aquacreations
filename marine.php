<h2 style="color: red">5 Best Saltwater Aquarium Fish</h2> 
<p style="color: rgb(45,50,250);">You’ve most likely heard that a saltwater aquarium is more difficult to keep than a freshwater aquarium. It is true that keeping the tank clean and water balanced does take a bit more work and dedication.However, you can make it a little easier on yourself by choosing to fill your tank with the best saltwater aquarium fish, having the right supplies and the right tank.If you’re just getting into keeping saltwater fish, here are some of the best varieties. </p>

<h1 style="color: brown">1 - clownfish</h1>
<img src="clown.jpg" height="200" width="300">
<li style="font-size: 18px; color: rgb(100,150,220);">Quick Info: Very hardy, easy to keep</li>
<li style="font-size: 18px; color:rgb(100,150,220); ">Scientific Name: Amphiprioninae</li>
<li style="font-size: 18px; color: rgb(100,150,220);">Care Level: Easy</li>
<li style="font-size: 18px; color: rgb(100,150,220);">Minimum Tank Size: 20 gallons</li>
<li style="font-size: 18px; color: rgb(100,150,220);">Diet: Omnivore</li>
<li style="font-size: 18px; color: rgb(100,150,220);">Temperament: Peaceful</li>
<p style="font-size: 18px; color: rgb(100,150,220);">The Clownfish is one of the most popular saltwater fish today; probably in no small part due to the Disney movie Finding Nemo.You may remember that Nemo was a Clownfish, and if you’ve been hoping to add one to your saltwater tank, you’re in luck.Clownfish(especially the tank raised variety)are easy to care for. Being one of the most popular saltwater fish, food for it is easy to find.Tank raised Ocellaris Clownfish are hardy and more colorful than the wild caught ones.As an added bonus,these colorful fish are active and don’t hide as much as their tank mates so they make an interesting addition to a beginner’s saltwater aquarium.</p>
