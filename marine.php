<img src="dotty.jpg" height="200" width="300">
<li style="font-size: 18px; color:rgb(100,150,220); ">Quick Info: Hardy, comes in different colors</li>
<li style="font-size: 18px; color:rgb(100,150,220); ">Scientific Name: Pseudochromidae
</li>
<li style="font-size: 18px; color:rgb(100,150,220); ">Care Level: Easy</li>
<li style="font-size: 18px; color:rgb(100,150,220); ">Minimum Tank Size: 30 gallons</li>
<li style="font-size: 18px; color:rgb(100,150,220); ">Diet: Carnivore</li>
<li style="font-size: 18px; color:rgb(100,150,220); ">Temperament: Semi-aggressive</li>
<p style="font-size: 18px; color: rgb(100,150,220);">Another good choice for the beginner saltwater enthusiast, the Dottyback is a hardy fish that comes in various colors including blue, purple, and yellow as well as bicolor that are half yellow and half purple.This fish will need a minimum aquarium size of 30 gallons. The Dottyback can be aggressive, but with the correct tank size, plenty of hiding spots, and a good diet, they’re typically happy and non-confrontational</p>
